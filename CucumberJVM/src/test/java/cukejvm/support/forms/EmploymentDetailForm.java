public class EmploymentDetailForm {
    private String occupationCategory;
    private String occupationName;
    private String occupationID;
    private String employerName;
    private String employerNumber;
    private String yearWithEmployer;
    private String monthWithEmployer;

    public String getOccupationCategory() {
        return occupationCategory;
    }

    public void setOccupationCategory(String occupationCategory) {
        this.occupationCategory = occupationCategory;
    }

    public String getOccupationName() {
        return occupationName;
    }

    public void setOccupationName(String occupationName) {
        this.occupationName = occupationName;
    }

    public String getOccupationID() {
        return occupationID;
    }

    public void setOccupationID(String occupationID) {
        this.occupationID = occupationID;
    }

    public String getEmployerName() {
        return employerName;
    }

    public void setEmployerName(String employerName) {
        this.employerName = employerName;
    }

    public String getEmployerNumber() {
        return employerNumber;
    }

    public void setEmployerNumber(String employerNumber) {
        this.employerNumber = employerNumber;
    }

    public String getYearWithEmployer() {
        return yearWithEmployer;
    }

    public void setYearWithEmployer(String yearWithEmployer) {
        this.yearWithEmployer = yearWithEmployer;
    }

    public String getMonthWithEmployer() {
        return monthWithEmployer;
    }

    public void setMonthWithEmployer(String monthWithEmployer) {
        this.monthWithEmployer = monthWithEmployer;
    }
}
