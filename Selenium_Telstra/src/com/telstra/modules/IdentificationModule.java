package com.telstra.modules;

public class IdentificationModule {
	private String documentType;
	private String documentCountry;
	private String documentNumber;
	private String documentPlaceOfIssue;
	private String documentExpireDay;
	private String documentExpireMonth;
	private String documentExpireYear;
	
	public String getDocumentType() {
		return documentType;
	}
	public void setDocumentType(String documentType) {
		this.documentType = documentType;
	}
	public String getDocumentCountry() {
		return documentCountry;
	}
	public void setDocumentCountry(String documentCountry) {
		this.documentCountry = documentCountry;
	}
	public String getDocumentNumber() {
		return documentNumber;
	}
	public void setDocumentNumber(String documentNumber) {
		this.documentNumber = documentNumber;
	}
	public String getDocumentPlaceOfIssue() {
		return documentPlaceOfIssue;
	}
	public void setDocumentPlaceOfIssue(String documentPlaceOfIssue) {
		this.documentPlaceOfIssue = documentPlaceOfIssue;
	}
	public String getDocumentExpireDay() {
		return documentExpireDay;
	}
	public void setDocumentExpireDay(String documentExpireDay) {
		this.documentExpireDay = documentExpireDay;
	}
	public String getDocumentExpireMonth() {
		return documentExpireMonth;
	}
	public void setDocumentExpireMonth(String documentExpireMonth) {
		this.documentExpireMonth = documentExpireMonth;
	}
	public String getDocumentExpireYear() {
		return documentExpireYear;
	}
	public void setDocumentExpireYear(String documentExpireYear) {
		this.documentExpireYear = documentExpireYear;
	}
}
